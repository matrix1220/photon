
bot = None
find_router = None
main_menu = None

def set_bot(bot_):
	global bot
	bot = bot_
	return bot_

def set_find_router(find_router_):
	global find_router
	find_router = find_router_
	return find_router_

def set_main_menu(main_menu_):
	global main_menu
	main_menu = main_menu_
	return main_menu_